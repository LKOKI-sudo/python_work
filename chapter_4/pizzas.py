# Description: A simple program to demonstrate looping through a list of pizzas and printing messages.

# Creating a list of pizzas
pizzas = [ "pepperoni" , 'margherita' , 'bbq chicken' ]
# using a for loop to print each pizza's name
for pizza in pizzas:
    print (f" I love {pizza.title()} pizza. \n")

# Printing a final message after the loop
print ( " I really love pizza! " )