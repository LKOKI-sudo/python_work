# Description: A simple program to demonstrate looping through a list of animals and printing messages.

# List of dog breeds
dogs = [ "Bulldog", "Beagle", "Poodle", "Labrador", "German Shepherd" ]
for dog in dogs:
    print(dog)
    print (f"a {dog} would make a great pet.\n")
# Final message after the loop
print("Any of these dogs would make a great pet!")